package org.example;

public class DPolinomio implements DatoPolinomio{
    public int termino_mayor;
    public int grado;

    public DPolinomio() {
        this.termino_mayor = termino_mayor;
        this.grado = grado;
    }

    public int getTermino_mayor() {
        return termino_mayor;
    }

    public int getGrado() {
        return grado;
    }

    public void setTermino_mayor(int termino_mayor) {
        this.termino_mayor = termino_mayor;
    }

    public void setGrado(int grado) {
        this.grado = grado;
    }
}
